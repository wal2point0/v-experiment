-- Run in Supabase SQL editor.
-- This schema is for demo/prototype usage from a public frontend.

create table if not exists public.menu_items (
  id bigint generated by default as identity primary key,
  number integer not null,
  name text not null,
  description text not null default 'Delicious food',
  price numeric(10, 2) not null,
  image text,
  created_at timestamptz not null default now()
);

create unique index if not exists menu_items_number_idx on public.menu_items(number);

create table if not exists public.orders (
  id bigint generated by default as identity primary key,
  items jsonb not null,
  total numeric(10, 2) not null,
  date timestamptz not null default now(),
  created_at timestamptz not null default now()
);

alter table public.menu_items enable row level security;
alter table public.orders enable row level security;

-- Demo policies: allow anonymous read/write.
-- Replace with authenticated/admin-only policies before production launch.
do $$
begin
  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'menu_items' and policyname = 'menu_items_demo_rw'
  ) then
    create policy menu_items_demo_rw
      on public.menu_items
      for all
      to anon, authenticated
      using (true)
      with check (true);
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'orders' and policyname = 'orders_demo_rw'
  ) then
    create policy orders_demo_rw
      on public.orders
      for all
      to anon, authenticated
      using (true)
      with check (true);
  end if;
end $$;
